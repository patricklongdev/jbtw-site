/**
 * Reads all MDX frontmatter in src/content/chapters/ and generates:
 *   functions/book.asp.js  — Cloudflare Pages Function for ?cid= redirects
 *
 * Cloudflare Pages _redirects does not support query-string matching, so we
 * use a Pages Function to map cid → clean URL at request time.
 *
 * Each CID maps to the specific subsection URL (not just the chapter root),
 * so ?cid=12 lands directly on the right page rather than a TOC.
 */
import { readdir, readFile, writeFile, mkdir } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const MDX_DIR = join(__dirname, '../src/content/chapters');
const FUNCTIONS_DIR = join(__dirname, '../functions');

await mkdir(FUNCTIONS_DIR, { recursive: true });

function extractField(fm, field) {
  const m = fm.match(new RegExp(`^${field}:\\s*(?:'([^']*)'|"([^"]*)"|(.+?)\\s*$)`, 'm'));
  return m ? (m[1] ?? m[2] ?? m[3] ?? '').trim() : '';
}

const files = await readdir(MDX_DIR).catch(() => []);

// First pass: collect all entries and count subsections per chapter
const entries = [];
for (const file of files.filter(f => f.endsWith('.mdx'))) {
  const content = await readFile(join(MDX_DIR, file), 'utf8');
  const fmMatch = content.match(/^---\n([\s\S]*?)\n---/);
  if (!fmMatch) continue;
  const fm = fmMatch[1];

  const cid            = fm.match(/^cid:\s*(\d+)/m)?.[1];
  const book           = extractField(fm, 'book');
  const sectorNum      = fm.match(/^sector_number:\s*(\d+)/m)?.[1];
  const chapterSlug    = extractField(fm, 'chapter_slug');
  const subsectionSlug = extractField(fm, 'subsection_slug');

  if (!cid || !book || !chapterSlug || !subsectionSlug) continue;
  entries.push({ cid, book, sectorNum, chapterSlug, subsectionSlug });
}

// Count subsections per chapter to distinguish single vs. multi-subsection URLs
const chapterSubcounts = new Map();
for (const e of entries) {
  const key = `${e.book}::${e.chapterSlug}`;
  chapterSubcounts.set(key, (chapterSubcounts.get(key) ?? 0) + 1);
}

function buildUrl(e) {
  const isSingle = (chapterSubcounts.get(`${e.book}::${e.chapterSlug}`) ?? 1) === 1;

  if (e.book === 'justice-behind-the-walls') {
    const base = '/publications/justice-behind-the-walls';
    if (e.chapterSlug === 'introduction') return `${base}/introduction/${e.subsectionSlug}/`;
    if (e.chapterSlug === 'conclusion')   return `${base}/conclusion/`;
    if (isSingle) return `${base}/sector-${e.sectorNum}/chapter-${e.chapterSlug}/`;
    return `${base}/sector-${e.sectorNum}/chapter-${e.chapterSlug}/${e.subsectionSlug}/`;
  } else {
    const base = '/publications/prisoners-of-isolation';
    if (e.chapterSlug === 'preface') return `${base}/preface/${e.subsectionSlug}/`;
    if (isSingle) return `${base}/chapter-${e.chapterSlug}/`;
    return `${base}/chapter-${e.chapterSlug}/${e.subsectionSlug}/`;
  }
}

// Build CID map: cid string → new URL
const cidMap = {};

// Navigation-only index pages (no body content — redirect to book index)
const navCids = {
  '1':   '/publications/justice-behind-the-walls/',
  '6':   '/publications/justice-behind-the-walls/',
  '16':  '/publications/justice-behind-the-walls/',
  '51':  '/publications/justice-behind-the-walls/',
  '111': '/publications/justice-behind-the-walls/',
  '184': '/publications/justice-behind-the-walls/',
  '253': '/publications/justice-behind-the-walls/',
  '760': '/publications/prisoners-of-isolation/',
};
Object.assign(cidMap, navCids);

for (const e of entries) {
  cidMap[e.cid] = buildUrl(e);
}

const fn = `// Auto-generated by scripts/generate-redirects.mjs — do not edit by hand
const CID_MAP = ${JSON.stringify(cidMap, null, 2)};

export async function onRequest(context) {
  const url = new URL(context.request.url);
  const cid = url.searchParams.get('cid');
  const dest = CID_MAP[cid];
  if (dest) {
    return Response.redirect(new URL(dest, url).toString(), 301);
  }
  return new Response('Not found', { status: 404 });
}
`;

await writeFile(join(FUNCTIONS_DIR, 'book.asp.js'), fn);
console.log(`generate-redirects: ${Object.keys(cidMap).length} CID mappings → functions/book.asp.js`);
